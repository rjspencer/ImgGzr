<h2>Graphic Design and Layout</h2>
<p>Graphic Design includes organized learning experiences that incorporate a variety 
of visual art techniques as they relate to the design and execution of layouts and illustrations for 
advertising, displays, promotional materials, and instructional manuals. Instruction also covers 
advertising theory and preparation of copy, lettering, posters, and artwork in addition to incorporation of 
photographic images. Communication skills will be emphasized through the study of effective methods 
used to design commercial products that impart information and ideas. </p>
<p>This class is taught on Mac computers running OSX using Adobe Creative Suite 4.  Adobe Illustrator is the most extensively utilized program.  Photoshop and InDesign are briefly covered, mostly as enhancers to Illustrator.</p>
